def _create_pdf(path: Path, lines: list[str], creator: str = "", producer: str = "",
                creation_date: str = "", mod_date: str = ""):
    """Create a simple PDF with the given text lines and metadata."""
    try:
        from fpdf import FPDF

        class ForgeShieldPDF(FPDF):
            pass

        pdf = ForgeShieldPDF()
        pdf.add_page()
        pdf.set_margins(20, 20, 20)
        pdf.set_auto_page_break(auto=True, margin=20)
        pdf.set_font("Courier", size=9)

        for line in lines:
            # Sanitize: remove non-latin-1 characters (em-dashes, etc.)
            safe_line = line.encode("latin-1", errors="replace").decode("latin-1")
            if safe_line.strip() == "":
                pdf.ln(3)
            elif safe_line.startswith("?") and len(safe_line) > 10 and safe_line[1] == "?":
                # Was a unicode separator line — replace with ASCII
                safe_line = "-" * 55
                pdf.set_font("Courier", size=9)
                pdf.cell(0, 5, safe_line, new_x="LMARGIN", new_y="NEXT")
            elif any(line.startswith(header) for header in ["EARNINGS", "DEDUCTIONS", "PART A", "PART B",
                                                             "PROPERTY DETAILS", "BUYER", "SELLER",
                                                             "CONSIDERATION", "GOVERNMENT", "CANARA BANK",
                                                             "GREENTECH", "FORM 16"]):
                pdf.set_font("Courier", "B", 10)
                pdf.cell(0, 7, safe_line, new_x="LMARGIN", new_y="NEXT")
                pdf.set_font("Courier", size=9)
            else:
                pdf.cell(0, 5, safe_line, new_x="LMARGIN", new_y="NEXT")


        raw_pdf = bytes(pdf.output())

        # Inject custom metadata using pypdf if available
        try:
            import pypdf
            from pypdf import PdfWriter, PdfReader
            import io

            reader = PdfReader(io.BytesIO(raw_pdf))
            writer = PdfWriter()
            for page in reader.pages:
                writer.add_page(page)

            writer.add_metadata({
                "/Creator": creator,
                "/Producer": producer,
                "/CreationDate": creation_date,
                "/ModDate": mod_date,
                "/Author": "ForgeShield Mock Data Generator",
            })

            out_buf = io.BytesIO()
            writer.write(out_buf)
            path.write_bytes(out_buf.getvalue())
        except Exception:
            # Fallback: save raw without custom metadata
            path.write_bytes(raw_pdf)

    except ImportError:
        # fpdf2 not installed: create a simple text file placeholder
        path.with_suffix(".txt").write_text(
            "\n".join(lines),
            encoding="utf-8",
        )
        print(f"    [WARNING] fpdf2 not installed. Created TXT placeholder: {path.with_suffix('.txt').name}")


