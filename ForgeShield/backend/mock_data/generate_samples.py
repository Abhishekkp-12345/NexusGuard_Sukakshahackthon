def gen_bank_statement():
    """Bank statement showing avg monthly credit of only ₹85,000 (contradicts inflated salary)."""
    lines = [
        "CANARA BANK",
        "Statement of Account",
        "Account No: 3891245670 | IFSC: CNRB0004521",
        "Account Holder: RAJESH KUMAR",
        "Statement Period: 01-Jan-2025 to 30-Jun-2025",
        "",
        "-----------------------------------------────────────────────────────",
        "Date          Description                   Credit      Debit       Balance",
        "-----------------------------------------────────────────────────────",
    ]

    # Generate 6 months of realistic transactions
    balance = 45000.0
    months = [
        ("Jan 2025", 85200, [12000, 8500, 3000, 2200, 1500]),
        ("Feb 2025", 85200, [12000, 8500, 3000, 2200, 5000]),
        ("Mar 2025", 84800, [12000, 8500, 3000, 2200, 4500]),
        ("Apr 2025", 85200, [12000, 8500, 3000, 2200, 6000]),
        ("May 2025", 85200, [12000, 8500, 3000, 2200, 3500]),
        ("Jun 2025", 85200, [12000, 8500, 3000, 2200, 7000]),
    ]

    for month, salary, debits in months:
        balance += salary
        lines.append(f"01-{month[:3]}-25  SALARY CREDIT - GREENTECH  {salary:>10,}               {balance:>12,.2f}  Cr")
        for i, debit in enumerate(debits):
            balance -= debit
            desc = ["EMI-Home Loan", "Electricity Bill", "Grocery", "Insurance", "Online Transfer"][i]
            lines.append(f"10-{month[:3]}-25  {desc:<30}              {debit:>8,}   {balance:>12,.2f}  Cr")
        lines.append("")

    total_credits = sum(m[1] for m in months)
    avg_credit = total_credits / 6
    lines.extend([
        "-----------------------------------------────────────────────────────",
        f"Total Credits (6 months): Rs. {total_credits:,.0f}",
        f"Average Monthly Credit  : Rs. {avg_credit:,.0f}",
        f"Closing Balance         : Rs. {balance:,.2f}",
        "",
        "This is a computer-generated statement. No signature required.",
    ])

    _create_pdf(
        OUTPUT_DIR / "bank_statement.pdf",
        lines=lines,
        creator="Canara Bank CBS System",
        producer="Canara Bank Digital Banking",
        creation_date="D:20250701000000+05'30'",
        mod_date="D:20250701000000+05'30'",
    )
    print(f"  [OK] bank_statement.pdf  [Avg monthly credit: Rs. {avg_credit:,.0f} — contradicts inflated salary]")


